#ref is the size of the reference station, X, the station to fix
def fixsize (ref,X):
	n=np.size(X)
	if(n>ref):
		Y=np.zeros(ref)
		for i in range(ref):
			Y[i]=X[i]
		return(Y)
	elif(n<ref):
		Y=np.zeros(ref)
		for i in range(n-1,ref-1,1):
			Y[i]=0
		return(Y)
	else:
		return(X)

